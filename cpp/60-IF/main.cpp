// exmaple : check if a naumber is even using " if " in cpp.
#include <iostream>
using namespace std;

int main() 
{
    int n;
    cout << "Enter a number: ";
    cin >> n;

    if (n % 2 == 0) 
    {
        cout << "The number is even";
    }

    return 0;
}


